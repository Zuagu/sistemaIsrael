package arcade.model;

/* @author Luis Cortez */
public class Cr {

    private int id_cr;
    private String cr;
    private String asignacion;
    private String numero_relacionado;
    private String estatus_ab;
    private String fecha_activacion;
    private String rfc;
    private String nombre;

    public Cr() {
    }

    public int getId_cr() {
        return id_cr;
    }

    public void setId_cr(int id_cr) {
        this.id_cr = id_cr;
    }

    public String getCr() {
        return cr;
    }

    public void setCr(String cr) {
        this.cr = cr;
    }

    public String getAsignacion() {
        return asignacion;
    }

    public void setAsignacion(String asignacion) {
        this.asignacion = asignacion;
    }

    public String getNumero_relacionado() {
        return numero_relacionado;
    }

    public void setNumero_relacionado(String numero_relacionado) {
        this.numero_relacionado = numero_relacionado;
    }

    public String getEstatus_ab() {
        return estatus_ab;
    }

    public void setEstatus_ab(String estatus_ab) {
        this.estatus_ab = estatus_ab;
    }

    public String getFecha_activacion() {
        return fecha_activacion;
    }

    public void setFecha_activacion(String fecha_activacion) {
        this.fecha_activacion = fecha_activacion;
    }

    public String getRfc() {
        return rfc;
    }

    public void setRfc(String rfc) {
        this.rfc = rfc;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
