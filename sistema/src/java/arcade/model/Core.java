package arcade.model;

/* @author Luis Cortez */
public class Core {

    String id_registro;
    String id_cliente;
    String region;
    String asignacion;
    String cuenta;
    String tipo_tel;
    String tipo_marcacion;
    String telefono;
    String f_repetido;
    String nombre;

    public Core() {

    }

    public String getId_registro() {
        return id_registro;
    }

    public void setId_registro(String id_registro) {
        this.id_registro = id_registro;
    }

    public String getId_cliente() {
        return id_cliente;
    }

    public void setId_cliente(String id_cliente) {
        this.id_cliente = id_cliente;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getAsignacion() {
        return asignacion;
    }

    public void setAsignacion(String asignacion) {
        this.asignacion = asignacion;
    }

    public String getCuenta() {
        return cuenta;
    }

    public void setCuenta(String cuenta) {
        this.cuenta = cuenta;
    }

    public String getTipo_tel() {
        return tipo_tel;
    }

    public void setTipo_tel(String tipo_tel) {
        this.tipo_tel = tipo_tel;
    }

    public String getTipo_marcacion() {
        return tipo_marcacion;
    }

    public void setTipo_marcacion(String tipo_marcacion) {
        this.tipo_marcacion = tipo_marcacion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getF_repetido() {
        return f_repetido;
    }

    public void setF_repetido(String f_repetido) {
        this.f_repetido = f_repetido;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
